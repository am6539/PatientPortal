CREATE TABLE [dbo].[Vital]
(
	[MedicalRecordId] INT NOT NULL,
	[PatientId] VARCHAR(20) NOT NULL , 
    [Height] VARCHAR(10) NOT NULL, 
    [Weight] VARCHAR(10) NOT NULL, 
    [BP_Systolic] VARCHAR(3) NOT NULL, -- Huyết áp tâm thu
	[BP_Diastolic] VARCHAR(3) NOT NULL, -- Huyết áp tâm trương
    [Temperature] VARCHAR(4) NOT NULL, -- Nhiệt độ
    [Pulse] VARCHAR(3) NOT NULL, -- Nhịp tim
    [BMI] VARCHAR(5) NOT NULL, 
    [Respiration] VARCHAR(2) NOT NULL -- Hô hấp - Nhịp thở
, 
    PRIMARY KEY ([MedicalRecordId]))
