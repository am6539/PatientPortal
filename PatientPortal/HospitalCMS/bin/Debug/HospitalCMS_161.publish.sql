					OR (P.Author like N'%' + @Search +'%' OR  @Search is null OR @Search = ''))
				) as tmppriority
				set @totalItem = (SELECT count(*) FROM #tmppriorityPost)
				SELECT  *
				FROM	#tmppriorityPost
				WHERE   RowNum BETWEEN (@PageIndex - 1) * @numberInPage + 1
						AND @PageIndex * @numberInPage ;
			END
		ELSE
		BEGIN
		
